package com.longan.web.core.form;

public class FlowRechargeForm {
	private String uid;
	private String itemRadio;
	private String price;
	private String otherSerialNO;

	public String getUid() {
		return uid;
	}

	public void setUid(String uid) {
		this.uid = uid;
	}

	public String getItemRadio() {
		return itemRadio;
	}

	public void setItemRadio(String itemRadio) {
		this.itemRadio = itemRadio;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getOtherSerialNO() {
		return otherSerialNO;
	}

	public void setOtherSerialNO(String otherSerialNO) {
		this.otherSerialNO = otherSerialNO;
	}

}
